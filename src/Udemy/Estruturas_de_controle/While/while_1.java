package Udemy.Estruturas_de_controle.While;

public class while_1 {
    public static void main(String[] args) {

        int contador = 1;
        while ( contador <= 10) {
            System.out.println("bom dia");
            // coloca-se o encremento ++ para ele contar somente as 10 vezes e nao infinito
            contador++;
// contador += 2  contar de 2 em 2

        }
    }
}
