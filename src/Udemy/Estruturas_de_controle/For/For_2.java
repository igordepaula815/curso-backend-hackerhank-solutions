package Udemy.Estruturas_de_controle.For;

public class For_2 {
// contador decrecente
    public static void main(String[] args) {
// laço até 10 ; a partir de zero ; contando decrecente de 2 em 2
        for ( int contador = 20 ; contador >= 0; contador -= 2 ) {
// %d recebe numero inteiro
            System.out.printf("contador = %d" , contador);

        }
    }
}
