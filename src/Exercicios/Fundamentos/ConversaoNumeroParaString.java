package Exercicios.Fundamentos;

public class ConversaoNumeroParaString {

    public static void main(String[] args) {
        Integer num1 = 10000;
        System.out.println(num1.toString());   // tranformou o integer e uma string
        System.out.println(num1.toString().length());   // conta as casa decimais

        // ou assim

        int num2 = 20000;
        System.out.println(Integer.toString(num2));   // assim oara tranformar um primitivo








    }
}
