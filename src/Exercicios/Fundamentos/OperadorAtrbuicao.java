package Exercicios.Fundamentos;

public class OperadorAtrbuicao {
    public static void main(String[] args) {

        // Atribuição simples
        int a = 10;

// Adição e atribuição
        int b = 5;
        a += b; // a = a + b

// Subtração e atribuição
        a -= b; // a = a - b

// Multiplicação e atribuição
        a *= b; // a = a * b

// Divisão e atribuição
        a /= b; // a = a / b

// Módulo e atribuição
        a %= b; // a = a % b
    }
}
